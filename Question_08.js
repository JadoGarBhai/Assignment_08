// Write a js program to check whether a character is uppercase or lowercase alphabet.

// VALUES 
let alphabet = "G";

// SOLUTION
// USING ELSE_IF CONDITION
if (alphabet >= "a" && alphabet <= "z") {
    console.log("Lower Case Alphabet");    
}else if (alphabet >= "A" && alphabet <= "Z") {
    console.log("Upper Case Alphabet");
}else{
    console.log("Bandy Da Putter Ban Ja Tu");
}